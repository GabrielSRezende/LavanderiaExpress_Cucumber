    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        String nome = txtNomeCliente.getText();
        String telefone = txtTelefoneCliente.getText();
        String endereco = txtEnderecoCliente.getText();

        try {
            String sql = "INSERT INTO clientes (nome, telefone, endereco) VALUES (?, ?, ?)";
            PreparedStatement stmt = ConexaoBanco.getConexao().prepareStatement(sql);
            stmt.setString(1, nome);
            stmt.setString(2, telefone);
            stmt.setString(3, endereco);
            stmt.executeUpdate();
            JOptionPane.showMessageDialog(this, "Cliente adicionado!");
            carregarUsuarios();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
        }
    }//GEN-LAST:event_btnAdicionarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int row = tblClientes.getSelectedRow();
        if (row == -1) return;

        int id = Integer.parseInt(tblClientes.getValueAt(row, 0).toString());
        String nome = txtNomeCliente.getText();
        String telefone = txtTelefoneCliente.getText();
        String endereco = txtEnderecoCliente.getText();

        try {
            String sql = "UPDATE clientes SET nome = ?, telefone = ?, endereco = ? WHERE id = ?";
            PreparedStatement stmt = ConexaoBanco.getConexao().prepareStatement(sql);
            stmt.setString(1, nome);
            stmt.setString(2, telefone);
            stmt.setString(3, endereco);
            stmt.setInt(4, id);
            stmt.executeUpdate();
            JOptionPane.showMessageDialog(this, "Cliente atualizado!");
            carregarUsuarios();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int row = tblClientes.getSelectedRow();
        if (row == -1) return;

        int id = Integer.parseInt(tblClientes.getValueAt(row, 0).toString());

        try {
            String sql = "DELETE FROM clientes WHERE id = ?";
            PreparedStatement stmt = ConexaoBanco.getConexao().prepareStatement(sql);
            stmt.setInt(1, id);
            stmt.executeUpdate();
            JOptionPane.showMessageDialog(this, "Cliente exclu√≠do!");
            carregarUsuarios();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

