    private void carregarOrdens() {
        DefaultListModel<String> modeloRecebido = new DefaultListModel<>();
        DefaultListModel<String> modeloLavando = new DefaultListModel<>();
        DefaultListModel<String> modeloSecando = new DefaultListModel<>();
        DefaultListModel<String> modeloConcluido = new DefaultListModel<>();

        try {
            String sql = "SELECT os.id, SUBSTRING_INDEX(c.nome, ' ', 1) as nome, os.data_criacao, os.status, s.nome as servico " +
                         "FROM ordens_servico os " +
                         "JOIN clientes c ON os.id_cliente = c.id " +
                         "JOIN servicos s ON os.id_servico = s.id";
            PreparedStatement ps = ConexaoBanco.getConexao().prepareStatement(sql);
            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                int id = rs.getInt("id");
                String nome = rs.getString("nome");
                String servico = rs.getString("servico");
                Date data = rs.getDate("data_criacao");
                String status = rs.getString("status");
                String item = id + " - " + nome + " - " + servico;

                switch (status) {
                    case "Recebido": modeloRecebido.addElement(item); break;
                    case "Lavando": modeloLavando.addElement(item); break;
                    case "Secando": modeloSecando.addElement(item); break;
                    case "Conclu칤do": modeloConcluido.addElement(item); break;
                }
            }

            listRecebido.setModel(modeloRecebido);
            listLavando.setModel(modeloLavando);
            listSecando.setModel(modeloSecando);
            listConcluido.setModel(modeloConcluido);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao carregar ordens: " + e.getMessage());
        }
    }

    private void avancarStatus() {
        javax.swing.JList<String> listaSelecionada = null;
        String statusAtual = null;
        String novoStatus = null;

        // Verifica qual lista tem um item selecionado
        if (!listRecebido.isSelectionEmpty()) {
            listaSelecionada = listRecebido;
            statusAtual = "Recebido";
            novoStatus = "Lavando";
        } else if (!listLavando.isSelectionEmpty()) {
            listaSelecionada = listLavando;
            statusAtual = "Lavando";
            novoStatus = "Secando";
        } else if (!listSecando.isSelectionEmpty()) {
            listaSelecionada = listSecando;
            statusAtual = "Secando";
            novoStatus = "Conclu칤do";
        } else if (!listConcluido.isSelectionEmpty()) {
            JOptionPane.showMessageDialog(this, "Ordem j치 est치 conclu칤da!");
            return;
        }

        moverOrdem(listaSelecionada, novoStatus);
    }

    private void voltarStatus() {
        javax.swing.JList<String> listaSelecionada = null;
        String statusAtual = null;
        String novoStatus = null;

        // Verifica qual lista tem um item selecionado
        if (!listConcluido.isSelectionEmpty()) {
            listaSelecionada = listConcluido;
            statusAtual = "Conclu칤do";
            novoStatus = "Secando";
        } else if (!listSecando.isSelectionEmpty()) {
            listaSelecionada = listSecando;
            statusAtual = "Secando";
            novoStatus = "Lavando";
        } else if (!listLavando.isSelectionEmpty()) {
            listaSelecionada = listLavando;
            statusAtual = "Lavando";
            novoStatus = "Recebido";
        } else if (!listRecebido.isSelectionEmpty()) {
            JOptionPane.showMessageDialog(this, "Ordem j치 est치 no status inicial!");
            return;
        }

        moverOrdem(listaSelecionada, novoStatus);
    }

    private void moverOrdem(javax.swing.JList<String> lista, String novoStatus) {
        String selecionado = lista.getSelectedValue();
        if (selecionado != null && !selecionado.isEmpty()) {
            try {
                int id = Integer.parseInt(selecionado.split(" - ")[0]);

                String sql = "UPDATE ordens_servico SET status = ? WHERE id = ?";
                PreparedStatement ps = ConexaoBanco.getConexao().prepareStatement(sql);
                ps.setString(1, novoStatus);
                ps.setInt(2, id);
                ps.executeUpdate();

                carregarOrdens(); // Recarrega as listas
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Erro ao atualizar status: " + e.getMessage());
            }
        }
    }
    
